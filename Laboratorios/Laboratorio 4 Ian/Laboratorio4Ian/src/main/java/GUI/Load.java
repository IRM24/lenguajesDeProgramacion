    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        GrupoPlanificaciones grupoDatos = new GrupoPlanificaciones();
        Planificador planificador = grupoDatos.getPlanificador().get(0);
        
        String selectedItem = jComboBox1.getSelectedItem().toString();
        if (selectedItem.equals("ActividadPersonal")) {
            
            String e1 = jTextField2.getText();
            String e2 = jTextField3.getText();
            String e3 = jTextField4.getText();
            
            ActividadPersonal actividad = new ActividadPersonal(e1,e2,e3);
            
            planificador.añadirElemento(actividad);
            
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }else if(selectedItem.equals("ActividadEmpresarial")){
            String e1 = jTextField2.getText();
            String e2 = jTextField3.getText();
            String e3 = jTextField4.getText();
            
            ActividadEmpresarial actividad = new ActividadEmpresarial(e1,e2,e3);
            
            planificador.añadirElemento(actividad);
           
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }else if(selectedItem.equals("PersonaDeNegocios")){
            String e1 = jTextField2.getText();
            String e2 = jTextField3.getText();
            String e3 = jTextField4.getText();
            
            PersonaDeNegocios persona = new PersonaDeNegocios(e1,e2,e3);
            
            planificador.añadirElemento(persona);
            
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }else if(selectedItem.equals("PersonaPersonal")){
            String e1 = jTextField2.getText();
            String e2 = jTextField3.getText();
            String e3 = jTextField4.getText();
            
            PersonaPersonal persona = new PersonaPersonal(e1,e2,e3);
            
            planificador.añadirElemento(persona);
            
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

