    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    GrupoPlanificaciones grupoDatos = new GrupoPlanificaciones();
    List<Planificador> planificadores = grupoDatos.getPlanificador();

    StringBuilder datosCompletos = new StringBuilder();
    
    for (Planificador planificador : planificadores) {
        datosCompletos.append(planificador.toString()).append("\n");
    }

    jTextField1.setText(datosCompletos.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        
        String selectedItem = jComboBox1.getSelectedItem().toString();
        if (selectedItem.equals("ActividadPersonal")) {

            jLabel1.setText("Actividad");
            jLabel2.setText("Fecha");
            jLabel3.setText("Detalle");
        }else if(selectedItem.equals("ActividadEmpresarial")){
            jLabel1.setText("Actividad");
            jLabel2.setText("Fecha");
            jLabel3.setText("Lugar");
        }else if(selectedItem.equals("PersonaDeNegocios")){
            jLabel1.setText("Nombre");
            jLabel2.setText("Ubicacion");
            jLabel3.setText("Puesto");
        }else if(selectedItem.equals("PersonaPersonal")){
            jLabel1.setText("Nombre");
            jLabel2.setText("Ubicacion");
            jLabel3.setText("Relacion");
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

